package com.dfined.omnipatcher.data.data_structure.game;

public class Slot {
    String name;
    String path;

    public Slot(String name, String path) {
        this.name = name;
        this.path = path;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getPath() {
        return path;
    }

    public void setPath(String path) {
        this.path = path;
    }
}
